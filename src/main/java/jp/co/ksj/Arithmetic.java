package jp.co.ksj;

public class Arithmetic {
	public static void main(String args[]) {
		int a = 11, b = 3;
		
		System.out.println("a + b"+ (a + b));
		System.out.println("a - b"+ (a -b));
		System.out.println("a * b"+ (a * b));
		System.out.println("a / b"+ (a / b));
		System.out.println("b /a"+ (b / a));
		System.out.println("a % b"+ (a % b));
		System.out.println("b % a"+ (b % a));
	}

}
